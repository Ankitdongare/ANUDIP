public class SwtichDemo {
    public static void main(String[] args) {

        char grade = 'A';
        switch (grade)
        {
            case 'B':
            System.out.println("Grade is b");
            break;
            case 'A':
                System.out.println("Grade is a");
                break;
            case 'C':
                System.out.println("Grade is c");
                break;
            default:
                System.out.println();
                
        }
      
        }
    }
